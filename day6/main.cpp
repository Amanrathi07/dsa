#include<iostream> 
using namespace std ;


int main(){
    
    // bitwise operators 
    // & | ^ 

// bitwise and
    cout<<(4 & 5)<<endl ;
    cout<<(4 & 8)<<endl ;

// bitwise or
    cout<<(4 | 5)<<endl ;
    cout<<(4 | 8)<<endl ;

//bitwise xor
    cout<<(4^8)<<endl ;

// bitwise leftshift  <<    and >> right shift 

// n<<i 
cout<<(6<<1)<<endl ;

    return 0 ;
}